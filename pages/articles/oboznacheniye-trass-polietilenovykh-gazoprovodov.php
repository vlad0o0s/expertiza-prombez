            <h2 class="article-content-title article-content-title-padding">Провода-спутники</h2>
            <div class="article-content-text">
            Для межпоселковых газопроводов при отсутствии постоянных мест привязки вдоль газопровода прокладывается изолированный провод-спутник, алюминиевый или медный, сечением 2,5 – 4 мм2. Он укладывается на расстоянии 0,2 – 0,3 м от газопровода, концы выводятся на поверхность под ковер или футляр вблизи от опознавательного знака в контрольных точках на расстоянии не более 4 км друг от друга. Провода-спутники при обрыве при земляных работах или нарушении проводимости из-за некачественного соединения при строительстве перестают выполнять свою функцию и требуют ремонта.<br><br>
            Провода-спутники — ненадежный способ обозначения газопровода. Они повреждаются при земляных работах, грызунами. Происходит старение и разрушение изоляции, что в агрессивной грунтовой среде приводит к нарушению проводимости. Аналогично возможно повреждение сигнальной ленты с прикатанным проводником.
            </div>

            <h2 class="article-content-title article-content-title-padding">Электронные маркеры</h2>
            <div class="article-content-text">
            Самый современный способ обозначения подземных полиэтиленовых газопроводов — электронные маркеры, которые закладываются в грунт при строительстве, реконструкции или ремонте. Обнаружение маркера происходит при зондировании поверхности земли радиочастотными сигналами, генерируемыми прибором-маркероискателем либо трассоискателем.<br><br>
            Маркеры для газо- и нефтепроводов имеют желтый цвет и работают на частоте 83 кГц. В зависимости от конструкции они бывают двух типов: шарового или дискового. Также маркеры подразделяются на пассивные и интеллектуальные. Если первые служат только для обнаружения трассы газопровода, то интеллектуальные позволяют идентифицировать объект. В них встроен чип с памятью, на котором можно записывать, хранить и считывать с него информацию. <br><br>
            Интеллектуальный шаровой маркер имеет диаметр 10,4 см. Внутри расположены резонансный колебательный контур и RFID-чип с памятью, находящиеся в незамерзающей жидкости. Плавание позволяет обеспечить горизонтальное положение электронных элементов, и значит, максимальный отраженный сигнал при поиске маркероискателем. Также упрощается закладка маркера при строительстве: его не надо ориентировать в пространстве. Существует другая конструкция: три взаимоперпендикулярных колебательных контура, что дает шарообразное электромагнитное поле. Благодаря этому положение маркера в грунте также может быть произвольным.
