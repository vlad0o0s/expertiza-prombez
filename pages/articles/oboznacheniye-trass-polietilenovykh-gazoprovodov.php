            <h2 class="article-content-title">Сигнальные ленты</h2>
            <div class="article-content-text">
                Для обозначения трасс полиэтиленовых газопроводов применяют сигнальную ленту, которую укладывают по всей длине трассы. СТО ГАЗПРОМ ГАЗОРАСПРЕДЕЛЕНИЕ 2.4-6-1–2019 «Лента сигнальная для трасс полиэтиленовых газопроводов. Технические требования и методы испытаний» устанавливает основные характеристики лент, применяемых при строительстве для газораспределительных организаций. Лента изготавливается желтой, на нее наносятся красные несмываемые надписи «Огнеопасно! Газ» на расстоянии не более 500 мм друг от друга. Надпись должна быть выделена полужирным шрифтом, высота букв — не менее 40% от ширины ленты. Лента изготавливается из полиэтилена толщиной не менее 0,2 мм шириной 200 мм. <br><br>

                Лента укладывается на расстоянии 0,2 м от верха присыпанного газопровода. При пересечении газопровода с подземными инженерными коммуникациями лента укладывается вдоль газопровода дважды на расстоянии не менее 0.2 м между собой и на 2 м в обе стороны от пересекаемого сооружения. Яркая расцветка и предупредительная надпись при проведении земляных работ позволяет вовремя обнаружить газопровод и избежать повреждений.<br><br>

                Обычная сигнальная лента позволяет обнаружить газопровод, только раскопав его. Приборное обнаружение трассоискателем возможно при использовании сигнальной ленты с вмонтированным в нее электропроводом-спутником или полосой металлической фольги. Они прикатываются к внутренней стороне ленты в заводских условиях. Стандартная лента имеет длину 250 м. При большой протяженности газопровода провода необходимо соединять между собой. Соединение производится двумя способами: скручиванием, после чего его изолируют поливинилхлоридной лентой ПВХ, или скотчлоками — специальными зажимными клеммниками для токопроводящих жил.
